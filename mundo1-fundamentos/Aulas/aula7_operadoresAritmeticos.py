#OPERADORES
numero1 = int(input('Um valor: '))
numero2 = int(input('Outro valor: '))

#operadores aritméticos
soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2
potenciacao = numero1 ** numero2
divisaoInteira = numero1 // numero2
divisaoModulo = numero1 % numero2

print('A soma é {},\na subtração é {},\no produto é {},\na divisão é {},\na potenciação é {},\na divisão inteira é {}\ne o resto da divisão é {}'.format(soma, subtracao, multiplicacao, divisao, potenciacao, divisaoInteira, divisaoModulo))
